package Assignments;

public class even {
    public static void main(String[] args)//FOR LOOP
    {
        for(int i=2;i<=10;i+=2)
        {
            System.out.println(i);
        }
        System.out.println();
        //Reverse Order
        for(int i=10;i>=2;i-=2)
        {
            System.out.println(i);
        }
    }
}
