package Assignments;

public class evenWhile {
    public static void main(String[] args)//FOR LOOP
    {
        int i=2,j=10;
        while(i<=10)
        {
            System.out.println(i);
            i+=2;
        }
        System.out.println();
        //Reverse Order
        while(j>=2)
        {
            System.out.println(j);
            j-=2;
        }
    }  
}
