package Assignments;

class loop{
    public static void main(String[] args)
    {
        int i=1,j=10;
        while(i<=10)//prints numbers from 1 to 10
        {
            System.out.println(i);
            i++;
        }
        System.out.println();
        while(j>=1)//prints numbers from 1 to 10 in reverse order
        {
            System.out.println(j);
            j--;
        }
    }
}
